    private void ViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewActionPerformed
        // get the table model and clear old data
        DefaultTableModel model = (DefaultTableModel) Table1.getModel();
        model.setRowCount(0);

        // connect to the database
        try (Connection conn = getConnection()) {
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Cannot connect to the database. Ensure MySQL is running.");
                return;
            }

            // SQL Query to get all students
            String query = "SELECT * FROM Student";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            // Loop through the database results row by row
            while (rs.next()) {
                int id = rs.getInt("student_id");
                String fname = rs.getString("first_name");
                String lname = rs.getString("last_name");
                int age = rs.getInt("age");
                String email = rs.getString("email");

                // Add each row into the Java GUI table
                model.addRow(new Object[]{id, fname, lname, age, email});
            }

            clearFields();

            if (evt != null) {
                JOptionPane.showMessageDialog(this, "Table Data Refreshed Successfully!");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }


    }//GEN-LAST:event_ViewActionPerformed

