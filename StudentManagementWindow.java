    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        String firstName = txtFirstName.getText().trim();
        String lastName = txtLastName.getText().trim();
        String ageString = txtAge.getText().trim();
        String email = txtEmail.getText().trim();

        // Validation to prevent empty rows
        if (firstName.isEmpty() || lastName.isEmpty() || ageString.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!");
            return;
        }

        int age;
        try {
            age = Integer.parseInt(ageString);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Age must be a valid number!");
            return;
        }

        // to call the ' Student ' table
        String query = "INSERT INTO Student (first_name, last_name, age, email) VALUES (?, ?, ?, ?)";

        try (Connection conn = getConnection()) {
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Database connection failed. Check your setup.");
                return;
            }

            try (PreparedStatement pst = conn.prepareStatement(query)) {
                pst.setString(1, firstName);
                pst.setString(2, lastName);
                pst.setInt(3, age);
                pst.setString(4, email);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Student Added Successfully!");

                // Clear the text fields after successful add
                txtFirstName.setText("");
                txtLastName.setText("");
                txtAge.setText("");
                txtEmail.setText("");

                // Refresh the table to show the new data
                ViewActionPerformed(null);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error adding student: " + e.getMessage());
        }
    }//GEN-LAST:event_AddActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        String firstName = txtFirstName.getText().trim();
        String lastName = txtLastName.getText().trim();
        String ageString = txtAge.getText().trim();
        String email = txtEmail.getText().trim();

        // Validation to prevent empty rows
        if (firstName.isEmpty() || lastName.isEmpty() || ageString.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!");
            return;
        }

        int age;
        try {
            age = Integer.parseInt(ageString);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Age must be a valid number!");
            return;
        }

        int selectedRow = Table1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student to update from the table.");
            return;
        }

        int studentId = (int) Table1.getValueAt(selectedRow, 0);
        String query = "UPDATE Student SET first_name=?, last_name=?, age=?, email=? WHERE student_id=?";

        try (Connection conn = getConnection()) {
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Database connection failed. Please check your setup.");
                return;
            }

            try (PreparedStatement pst = conn.prepareStatement(query)) {
                pst.setString(1, firstName);
                pst.setString(2, lastName);
                pst.setInt(3, age);
                pst.setString(4, email);
                pst.setInt(5, studentId);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Student Updated Successfully!");
                ViewActionPerformed(null); // Refresh table
                clearFields();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error updating student: " + e.getMessage());
        }


    }//GEN-LAST:event_UpdateActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = Table1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student to delete.");
            return;
        }
        int studentId = (int) Table1.getValueAt(selectedRow, 0);

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = getConnection(); PreparedStatement pst = conn.prepareStatement("DELETE FROM Student WHERE student_id=?")) {
                pst.setInt(1, studentId);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Student Deleted!");
                ViewActionPerformed(null);
                clearFields();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_DeleteActionPerformed

